    {
         $request->validate([
                                    
                    'first_name'=>'required',
                    'last_name'=>'required',
                    'phone_number'=>'required',
                    'email'=>'required',
                    'message' => 'required',
        
                        ]);

        $input=$request->all();
        Send::create($input);
        
        return redirect('')->with('status', 'Query Send  Successfully');
